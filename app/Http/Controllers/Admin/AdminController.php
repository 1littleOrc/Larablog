	public function storeAdminUser(CreateFirstAdminRequest $request)
	{
		$user = new User;
		$user->email = Request::get('email');
		$user->password = Hash::make(Request::get('password'));
		$user->superuser = true;
		$user->username = "Admin";
		$user->save();

		Auth::login($user);

		return Redirect::to('admin/index');
	}


